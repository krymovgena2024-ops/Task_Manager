# 1. РОБОТА ІЗ ЗМІНАМИ (БАЗОВИЙ ЦИКЛ)
# -----------------------------------------------------------------
# git add .                 # Додати всі змінені файли до "черги"
# git commit -m "Опис"      # Створити "зліпок" (коміт) із назвою зміни
# git push origin main      # Відправити зміни в основну гілку на GitHub


# 2. РОБОТА З ГІЛКАМИ (ЕКСПЕРИМЕНТИ)
# -----------------------------------------------------------------
# Створити нову гілку для тесту (наприклад, 'refactoring'):
# git checkout -b refactoring

# Переключитися назад на головну гілку:
# git checkout main

# Подивитися список усіх гілок:
# git branch


# 3. ЗЛИТТЯ ГІЛОК (MERGE) — ПЕРЕНЕСЕННЯ ЗМІН У MAIN
# -----------------------------------------------------------------
# Крок 1: Перейди в main
# git checkout main

# Крок 2: Онови локальний main з сервера (про всяк випадок)
# git pull origin main

# Крок 3: Влий зміни з гілки 'refactoring' в 'main'
# git merge refactoring

# Крок 4: Відправ оновлений main на GitHub
# git push origin main


# 4. ВІДКАТ (ЯКЩО ЩОСЬ ПІШЛО НЕ ТАК)
# -----------------------------------------------------------------
# Скасувати зміни у файлі, які ще не були додані в commit:
# git checkout -- назва_файлу.py

# Видалити останній коміт (але залишити код):
# git reset --soft HEAD~1

# ПОВНИЙ відкат до останнього коміту (код видалиться! обережно!):
# git reset --hard HEAD


# 5. КОРИСНІ КОМАНДИ
# -----------------------------------------------------------------
# git log --oneline         # Короткий список останніх комітів
# git remote -v             # Перевірити, до якого репозиторію прив'язаний проєкт